def area(a, b):
    """
    Вычисляет площадь прямоугольника.
    
    Args:
        a (float): Длина прямоугольника (должна быть неотрицательной)
        b (float): Ширина прямоугольника (должна быть неотрицательной)
    
    Returns:
        float: Площадь прямоугольника
    
    Raises:
        ValueError: Если длина или ширина отрицательные
    """
    if a < 0 or b < 0:
        raise ValueError("Длина и ширина не могут быть отрицательными")
    return a * b


def perimeter(a, b):
    """
    Вычисляет периметр прямоугольника.
    
    Args:
        a (float): Длина прямоугольника (должна быть неотрицательной)
        b (float): Ширина прямоугольника (должна быть неотрицательной)
    
    Returns:
        float: Периметр прямоугольника
    
    Raises:
        ValueError: Если длина или ширина отрицательные
    """
    if a < 0 or b < 0:
        raise ValueError("Длина и ширина не могут быть отрицательными")
    return 2 * (a + b)