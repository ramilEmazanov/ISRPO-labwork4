def area(a, h):
    """
    Вычисляет площадь треугольника.
    
    Args:
        a (float): Основание треугольника (должно быть неотрицательным)
        h (float): Высота треугольника (должна быть неотрицательной)
    
    Returns:
        float: Площадь треугольника
    
    Raises:
        ValueError: Если основание или высота отрицательные
    """
    if a < 0 or h < 0:
        raise ValueError("Основание и высота не могут быть отрицательными")
    return a * h / 2


def perimeter(a, b, c):
    """
    Вычисляет периметр треугольника.
    
    Args:
        a (float): Первая сторона треугольника (должна быть неотрицательной)
        b (float): Вторая сторона треугольника (должна быть неотрицательной)
        c (float): Третья сторона треугольника (должна быть неотрицательной)
    
    Returns:
        float: Периметр треугольника
    
    Raises:
        ValueError: Если любая из сторон отрицательная
    """
    if a < 0 or b < 0 or c < 0:
        raise ValueError("Стороны треугольника не могут быть отрицательными")
    return a + b + c 